/**
 * Транслитерирует кириллицу в латиницу, приводит к нижнему регистру,
 * заменяет пробелы на дефисы и удаляет спецсимволы.
 * @param input - Входная строка (может быть на кириллице или содержать спецсимволы).
 * @returns Преобразованная строка в нижнем регистре (например, "Привет, Мир!" → "privet-mir").
 */
export function transliterateToSlug(input: string): string {
    if (!input) return '';

    // Словарь для транслитерации (все символы ведём к нижнему регистру)
    const cyrillicMap: Record<string, string> = {
        а: 'a',
        б: 'b',
        в: 'v',
        г: 'g',
        д: 'd',
        е: 'e',
        ё: 'yo',
        ж: 'zh',
        з: 'z',
        и: 'i',
        й: 'y',
        к: 'k',
        л: 'l',
        м: 'm',
        н: 'n',
        о: 'o',
        п: 'p',
        р: 'r',
        с: 's',
        т: 't',
        у: 'u',
        ф: 'f',
        х: 'h',
        ц: 'ts',
        ч: 'ch',
        ш: 'sh',
        щ: 'sch',
        ъ: '',
        ы: 'y',
        ь: '',
        э: 'e',
        ю: 'yu',
        я: 'ya',
        // Заглавные буквы (транслитерируем так же, но в toLowerCase их результат станет lowercase)
        А: 'a',
        Б: 'b',
        В: 'v',
        Г: 'g',
        Д: 'd',
        Е: 'e',
        Ё: 'yo',
        Ж: 'zh',
        З: 'z',
        И: 'i',
        Й: 'y',
        К: 'k',
        Л: 'l',
        М: 'm',
        Н: 'n',
        О: 'o',
        П: 'p',
        Р: 'r',
        С: 's',
        Т: 't',
        У: 'u',
        Ф: 'f',
        Х: 'h',
        Ц: 'ts',
        Ч: 'ch',
        Ш: 'sh',
        Щ: 'sch',
        Ъ: '',
        Ы: 'y',
        Ь: '',
        Э: 'e',
        Ю: 'yu',
        Я: 'ya',
    };

    let result = '';
    for (const char of input.toLowerCase()) {
        // Сразу приводим к lowercase
        if (cyrillicMap[char]) {
            result += cyrillicMap[char]; // Добавляем транслитерированный символ
        } else if (/[a-z0-9]/.test(char)) {
            // Проверяем только lowercase и цифры
            result += char;
        } else if (char === ' ' || char === '-') {
            result += '-';
        }
        // Остальные символы игнорируем
    }

    // Чистка результата:
    return result
        .replace(/-+/g, '-') // Убираем повторяющиеся дефисы
        .replace(/^-|-$/g, ''); // Убираем дефисы в начале и конце
}
